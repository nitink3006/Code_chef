#include <iostream>
using namespace std;

int main() {
    int t;
    cin>>t;
    for(int i=0;i<t;i++){
        int x;
        cin>>x;
        if(x>=30){
            cout<<"YES"<<endl;
        }
        else if(x<=30){
            cout<<"NO"<<endl;
        }
    }
	return 0;
}



